/**
 * See also:
 * http://rosalind.info/problems/lcsm/
 * Finding a Shared Motif  (Longest Common Substring)
 *
 * Problem

A common substring of a collection of strings is a substring of every
member of the collection. We say that a common substring is a longest
common substring if there does not exist a longer common substring.
For example, "CG" is a common substring of "ACGTACGT" and "AACCGTATA",
but it is not as long as possible; in this case, "CGTA" is a longest
common substring of "ACGTACGT" and "AACCGTATA".

Note that the longest common substring is not necessarily unique;
for a simple example, "AA" and "CC" are both longest common
substrings of "AACC" and "CCAA".

Given: A collection of k (kâ‰¤100) DNA strings of length at most 1 kbp each in FASTA format.

Return: A longest common substring of the collection. (If multiple solutions exist, you may return any single solution.)

 */

internal class RosieFindSharedMotifTest {

    lateinit var fsm: FindSharedMotif

    @BeforeEach
    fun setUp() {
        fsm = FindSharedMotif()
    }

    @AfterEach
    fun tearDown() {
    }

    @Test
    @DisplayName("Rosalind Concensus sample test")
    fun rosalindConsensusSampleTest() {
        val sampleInput = """
>Rosalind_1
GATTACA
>Rosalind_2
TAGACCA
>Rosalind_3
ATACA
        """.trimIndent().lines()

        val dnaList = utilityParseFASTA(sampleInput)


        val consensusString = fsm.findMostLikelyCommonAncestor(dnaList)

        //println(consensusString)
        assertEquals("TA", consensusString)

    }

    @Test
    @DisplayName("Rosalind Concensus corner case test")
    fun rosalindConsensusCornerCaseTest() {
        val sampleInput = """
>Rosalind_1
ACGT
>Rosalind_2
ACGT
>Rosalind_3
ACGT
        """.trimIndent().lines()

