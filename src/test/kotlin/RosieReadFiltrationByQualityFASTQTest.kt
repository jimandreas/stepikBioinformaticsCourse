+
B:EI>JDB5=>DA?E6B@@CA?C;=;@@C:6D:3=@49;@87;::;;?8+
        """.trimIndent().lines().toMutableList()

        val thresholdAndPercentage = sampleInput[0].split(" ").map { it.toInt() }
        val qualityLevelExpected = thresholdAndPercentage[0]
        val threshold = thresholdAndPercentage[1]
        sampleInput.removeFirst()

        var numberPassing = 0
        while (sampleInput.size != 0) {
            val qualityLine = sampleInput[3]
            val passing = faq.testQuality(qualityLine, qualityLevelExpected, threshold)
            if (passing) {
                numberPassing++
            }
            sampleInput.removeFirst()
            sampleInput.removeFirst()
            sampleInput.removeFirst()
            sampleInput.removeFirst()
        }

//        println(numberPassing)
        assertEquals(2, numberPassing) // sample answer


    }


