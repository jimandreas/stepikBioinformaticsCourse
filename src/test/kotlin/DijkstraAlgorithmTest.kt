    @Test
    @DisplayName("DijkstraAlgorithm sample test")
    fun dijkstraAlgorithmSampleTest() {
        val sampleInput = """
            6 10
            3 4 4
            1 2 4
            1 3 2
            2 3 3
            6 3 2
            3 5 5
            5 4 1
            3 2 1
            2 4 2
            2 5 3
        """.trimIndent().lines().toMutableList()

        val expectedOutputString = """
            0 3 2 5 6 -1
        """.trimIndent()

        val numNodesAndLines = sampleInput[0].split(" ").map{ it.toInt()}
        val numNodes = numNodesAndLines[0]
        sampleInput.removeFirst()

        val graph = parseDijkstraGraphInput(numNodes, sampleInput)

        val result = dj.dijkstraAlgorithmFindShortestPaths(graph)

        println(result.joinToString(" "))

        val expectedResult = expectedOutputString.split(" ").map { it.toInt() }
        assertEquals(expectedResult, result)

    }


    /**
     * take the list of edges (from to weight) and convert to
     * a map of the base node to a hashMap of toNode and weight
     */
    private fun parseDijkstraGraphInput( numNodes: Int, lines: List<String>): HashMap<Int, HashMap<Int, Int>> {
        val map: HashMap<Int, HashMap<Int, Int>> = hashMapOf()

        // make sure all nodes have a list
        for (i in 1..numNodes) {
            map[i] = hashMapOf()
        }

        for (i in 0 until lines.size) {
            val l = lines[i]
            val p = l.split(" ").map { it.toInt() }
            if (!map.containsKey(p[0])) {
                map[p[0]] = hashMapOf(Pair(p[1], p[2]))
            } else {
                map[p[0]]!![p[1]] = p[2]  // add connection to p1 with weight p2
            }
        }

        return map
    }


